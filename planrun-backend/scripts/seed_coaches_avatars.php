$updated = 0;
foreach ($updates as $slug => $data) {
    $stmt = $db->prepare("SELECT id, avatar_path FROM users WHERE username_slug = ? AND role = 'coach'");
    $stmt->bind_param("s", $slug);
    $stmt->execute();
    $row = $stmt->get_result()->fetch_assoc();
    $stmt->close();

    if (!$row) {
        echo "Пропуск: $slug (не найден)\n";
        continue;
    }

    $userId = (int)$row['id'];

    // Скачиваем аватар (randomuser.me: men/women 1-99)
    $gender = ($data['gender'] ?? 'male') === 'female' ? 'women' : 'men';
    $imgNum = $data['avatar_img'];
    $avatarUrl = "https://randomuser.me/api/portraits/{$gender}/{$imgNum}.jpg";
    $ctx = stream_context_create(['http' => ['timeout' => 5]]);
    $imageData = @file_get_contents($avatarUrl, false, $ctx);

    $avatarPath = null;
    if ($imageData && strlen($imageData) > 1000) {
        $fileName = 'avatar_' . $userId . '_' . time() . '.jpg';
        $saved = @file_put_contents($avatarDir . $fileName, $imageData);
        if (!$saved) {
            $saved = @file_put_contents($tmpDir . $fileName, $imageData);
        }

        if ($saved) {
            $avatarPath = '/uploads/avatars/' . $fileName;

            // Удаляем старый аватар если был (локальный файл)
            if (!empty($row['avatar_path']) && strpos($row['avatar_path'], '/uploads/') === 0) {
                $oldPath = $projectRoot . $row['avatar_path'];
                if (is_file($oldPath)) {
                    @unlink($oldPath);
                }
            }
        } else {
            fwrite(STDERR, "  Ошибка записи. Используем внешний URL.\n");
            $avatarPath = $avatarUrl;
        }
    } else {
        $avatarPath = $avatarUrl;
    }

    if ($avatarPath) {
        $upd = $db->prepare("UPDATE users SET coach_bio = ?, avatar_path = ? WHERE id = ?");
        $upd->bind_param("ssi", $data['bio'], $avatarPath, $userId);
    } else {
        $upd = $db->prepare("UPDATE users SET coach_bio = ? WHERE id = ?");
        $upd->bind_param("si", $data['bio'], $userId);
    }

    if ($upd->execute()) {
        $updated++;
        echo "OK: $slug\n";
    } else {
        fwrite(STDERR, "Ошибка: $slug - " . $upd->error . "\n");
    }
    $upd->close();
}

echo "\nОбновлено тренеров: $updated\n";

