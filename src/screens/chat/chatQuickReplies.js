/**
 * Генерирует контекстные quick-reply кнопки на основе последнего сообщения AI.
 * Возвращает массив строк — текст кнопок, которые отправятся как сообщение пользователя.
 */

const CONFIRMATION_RE = /подтверди|подтвердишь|хочешь.*\?|давай.*\?|согласен|устраивает|перенес[тиу].*\?|обновить|пересчит|заменить.*\?|удалить.*\?|готов\?/ui;
const PLAN_QUESTION_RE = /план.*на.*недел|запланирован|расписание|что.*стоит/ui;
const WORKOUT_DETAIL_RE = /тренировк[аиу].*сегодня|сегодня.*тренировк|на.*завтра|лёгкий бег|длительный|интервал|темповый|фартлек/ui;
const FEELING_RE = /как.*самочувстви|как.*ноги|как.*прошл|как.*себя.*чувству|устал|боль|травм/ui;
const DONE_RE = /выполнен|перенес[ёе]н|обновлён|обновлен|удалён|удален|заменён|заменен|переставлен|готово|сделано|установил/ui;
const GOOD_RESULT_RE = /отлично|молодец|супер|прогресс|результат/ui;

export function getQuickReplies(aiMessage) {
  if (!aiMessage) return [];
  const text = aiMessage.trim();
  if (!text) return [];

  // После подтверждения и выполнения — предложить следующие шаги
  if (DONE_RE.test(text)) {
    return ['Покажи план на неделю', 'Что на завтра?'];
  }

  // AI спрашивает подтверждение → да/нет
  if (CONFIRMATION_RE.test(text)) {
    return ['Да, давай', 'Нет, оставь как есть'];
  }

  // AI спрашивает о самочувствии
  if (FEELING_RE.test(text)) {
    return ['Всё хорошо', 'Устал, нужен отдых', 'Немного болят ноги'];
  }

  // AI хвалит результат
  if (GOOD_RESULT_RE.test(text)) {
    return ['Что на завтра?', 'Покажи план на неделю'];
  }

  // AI показывает план/расписание
  if (PLAN_QUESTION_RE.test(text)) {
    return ['Перенеси тренировку', 'Поставь выходной на сегодня'];
  }

  // AI рассказывает о конкретной тренировке
  if (WORKOUT_DETAIL_RE.test(text)) {
    return ['Перенеси на завтра', 'Замени на лёгкий бег'];
  }

  // Дефолт — универсальные подсказки
  return ['Что на сегодня?', 'Покажи план на неделю'];
}

